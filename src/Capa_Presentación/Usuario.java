    public void ListarDemandado()
    {
        DefaultTableModel tabla1=new DefaultTableModel();
        DataEmpleado objdem=new DataEmpleado();
        ArrayList<DataEmpleado> lista3=new ArrayList();
        lista3=objdem.ListaEmpl();
        tabla1.addColumn("ID");
        tabla1.addColumn("Nombre");
        tabla1.addColumn("Apellido");
        tabla1.addColumn("Tipo ID");
        tabla1.addColumn("Correo");
        tabla1.addColumn("Direccion");
        tabla1.addColumn("Telefono");
        tabla1.setRowCount(lista3.size());
        int i=0;
        for (DataEmpleado x: lista3) 
        {
            tabla1.setValueAt(x.getEmpl_id_empl(), i, 0);
            tabla1.setValueAt(x.getEmpl_Nom(), i, 1);
            tabla1.setValueAt(x.getEmpl_ape(), i, 2);
            tabla1.setValueAt(x.getEmpl_tipo_id(), i, 3);
            tabla1.setValueAt(x.getEmpl_correo(), i, 4);
            tabla1.setValueAt(x.getEmpl_direc(), i,5);
            tabla1.setValueAt(x.getEmpl_tel(), i,5);
            i++;
        }
        
    }
    private void llenado(){
        Conexion con = new Conexion();
    try {
            ResultSet cad = con.listar("Select * from Empleado ");
            
                while (cad.next()) {

                    IdEntex.setText(cad.getString("id_empleado"));
                    NomEmtex.setText(cad.getString("nombre"));
                    TipIDtex.setText(cad.getString("tipo_id"));
                    CorreoEmtex.setText(cad.getString("correo_electronico"));
                    DirEmtex.setText(cad.getString("direccion"));
                    TelEmtex.setText(cad.getString("telefono"));
                    
                }
                    ResultSet c = con.listar("Select * from Contrato_modifica " );
            while (c.next()) {
            NContex.setText(c.getString("Num_Contrato_modifica"));
            Estatex.setText(c.getString("Estado"));
            TipCotex.setText(c.getString("Tipo_contrato"));
            
        }
                ResultSet ca = con.listar("Select * from  cargo ");
                while (ca.next()) {
                    
                CargEmtex.setText(ca.getString("Nombre_cargo"));
                
        }
        
                
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }

