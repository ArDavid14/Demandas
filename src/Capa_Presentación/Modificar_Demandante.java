    public void ListarDemandante()
    {
        DefaultTableModel tabla=new DefaultTableModel();
        DataDemandante objart=new DataDemandante();
        ArrayList<DataDemandante> lista2=new ArrayList();
        lista2=objart.ListaDemandante();
        tabla.addColumn("ID");
        tabla.addColumn("Nombre");
        tabla.addColumn("Tipo ID");
        tabla.addColumn("Correo");
        tabla.addColumn("Direccion");
        tabla.addColumn("Telefono");
        tabla.setRowCount(lista2.size());
        int i=0;
        for (DataDemandante x: lista2) 
        {
            tabla.setValueAt(x.getDemt_ID(), i, 0);
            tabla.setValueAt(x.getDemdt_Nom(), i, 1);
            tabla.setValueAt(x.getDemdt_TipoID(), i,2);
            tabla.setValueAt(x.getDemdt_Correo(), i, 3);
            tabla.setValueAt(x.getDemdt_Direc(), i, 4);
            tabla.setValueAt(x.getDemdt_Telef(), i, 5);
            i++;
        }
        this.jTable1.setModel(tabla);
    }

