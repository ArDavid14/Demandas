    public void ListarDetalleProceso()
    {
        DefaultTableModel tabla1=new DefaultTableModel();
        DataDetalleProceso objdem=new DataDetalleProceso();
        ArrayList<DataDetalleProceso> lista6=new ArrayList();
        lista6=objdem.ListaDetalleproceso();
        tabla1.addColumn("Estado");
        tabla1.addColumn("Num_Radicado");
        tabla1.addColumn("ID_Demdt");
        tabla1.addColumn("ID_ApoDemdt");
        tabla1.addColumn("ID_Demdd");
        tabla1.addColumn("ID_ApoDemdd");
        tabla1.setRowCount(lista6.size());
        int i=0;
        for (DataDetalleProceso x: lista6) 
        {
            tabla1.setValueAt(x.getDetalle_estado(), i, 0);
            tabla1.setValueAt(x.getDetalle_fknumradi(), i, 1);
            tabla1.setValueAt(x.getDetalle_fkiddemandante(), i, 2);
            tabla1.setValueAt(x.getDetalle_fkidapdemandante(), i, 3);
            tabla1.setValueAt(x.getDetalle_fkiddemandado(), i, 4);
            tabla1.setValueAt(x.getDetalle_fkidapdemandado(), i, 5);
            i++;
        }
        this.Tabla_Detalle.setModel(tabla1);
    }
    

